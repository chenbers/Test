package com.inthinc.pro.backing;


public class GoogleMapURLBean {

    private String mapUrl;
    private String mapV3Url;
    private String mapGeoUrl;
    
    public String getMapUrl() {
        return mapUrl;
    }

    public void setMapUrl(String mapUrl) {
        this.mapUrl = mapUrl;
    }

    public String getMapV3Url() {
        return mapV3Url;
    }

    public void setMapV3Url(String mapV3Url) {
        this.mapV3Url = mapV3Url;
    }

    public String getMapGeoUrl() {
        return mapGeoUrl;
    }

    public void setMapGeoUrl(String mapGeoUrl) {
        this.mapGeoUrl = mapGeoUrl;
    }




}
