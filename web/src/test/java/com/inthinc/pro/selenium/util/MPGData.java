package com.inthinc.pro.selenium.util;

public class MPGData {
    private String name;
    private Float light;
    private Float medium;
    private Float heavy;
    
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public Float getLight() {
        return light;
    }
    public void setLight(Float light) {
        this.light = light;
    }
    public Float getMedium() {
        return medium;
    }
    public void setMedium(Float medium) {
        this.medium = medium;
    }
    public Float getHeavy() {
        return heavy;
    }
    public void setHeavy(Float heavy) {
        this.heavy = heavy;
    }
    
}
